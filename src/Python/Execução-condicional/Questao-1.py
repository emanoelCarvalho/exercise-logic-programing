# Escreva um programa que pede ao usuário para inserir um número inteiro e, em seguida, informa se o número é par ou ímpar.

numero = int(input("Digite um numero: ")) # Recebe o numero que será verificado se é par ou impar

if (numero % 2 == 0) : 
    print("O numero é par") # Imprime que o numero é par
else : 
    print("O numero é impar")

