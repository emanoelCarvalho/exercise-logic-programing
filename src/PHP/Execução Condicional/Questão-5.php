<?php 
//Faça um programa que indentifique se um número é positivo, negativo ou zero

$numero = 0; //Variável que recebe o número

if($numero > 0){ //Condição para saber se o número é positivo
    echo "O número é positivo"; //Mensagem que será exibida caso o número seja positivo
}else if($numero < 0){ //Condição para saber se o número é negativo
    echo "O número é negativo"; //Mensagem que será exibida caso o número seja negativo
}else{
    echo "O número é zero"; //Mensagem que será exibida caso o número seja zero
};
?>