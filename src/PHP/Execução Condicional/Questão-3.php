<?php 
// Faça um programa que dado um número, de 0 á 7 mostre qual o dia da semana correspondente

$numero = 0; //Variável que recebe o número

switch ($numero) {
    case 0:
        echo "Domingo";
        break;
    case 1:
        echo "Segunda";
        break;
    case 2:
        echo "Terça";
        break;
    case 3:
        echo "Quarta";
        break;
    case 4:
        echo "Quinta";
        break;
    case 5:
        echo "Sexta";
        break;
    case 6:
        echo "Sábado";
        break;
}
?>