// Receber quatro números como parâmetro e retornar a média dos quatro como resultado;

// Função para calcular a média de quatro números
function mediaQuatroNumeros(numero1, numero2, numero3, numero4) {
    let media;
    media = (numero1 + numero2 + numero3 + numero4) / 4;
    return media;
}

// Chamada da função com valores de exemplo
console.log(mediaQuatroNumeros(6, 6, 6, 6));
 