/* Multiplicar dois números e verificar se o resultado é um número negativo; */


// Declarando duas variáveis 'numero1' e 'numero2' com valores 3 e -3, respectivamente.
let numero1 = 3;
let numero2 = -3;

// Multiplicando os dois números e armazenando o resultado em 'multiplicar'.
let multiplicar = numero1 * numero2;

// Verificando se 'multiplicar' é menor que 0 (negativo).
if (multiplicar < 0) {
    console.log('É negativo');
} else {
    console.log('É positivo');
}
