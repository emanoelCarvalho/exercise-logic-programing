 /* Elevar um número ao quadrado e informar se o resultado é maior do que 100. */

// Declarando uma variável 'numero' com o valor 0.
let numero = 0;

// Calculando o quadrado do número e armazenando o resultado em 'quadrado'.
let quadrado = numero ** 2;

// Utilizando uma estrutura condicional para verificar se 'quadrado' é maior que 100.
if (quadrado > 100) {
    console.log('É maior');
} else {
    console.log('Não é maior');
}
