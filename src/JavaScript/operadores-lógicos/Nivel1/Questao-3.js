/*Identificar se um número é divisível por 7 ou 8*/

// Defina o número que você deseja verificar se é divisível por 7 ou 8.
let numero = 14;

// Use uma estrutura condicional (if-else) para verificar a divisibilidade.
if (numero % 7 === 0 || numero % 8 === 0) {
    console.log('É divisível.'); // Se o número for divisível por 7 ou 8, imprime 'É divisível'.
} else {
    console.log('Não é divisível.'); // Caso contrário, imprime 'Não é divisível'.
}
