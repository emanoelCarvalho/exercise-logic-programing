/*Identificar quando duas variáveis são negativas ao mesmo tempo;*/

// Defina as duas variáveis que você deseja verificar se são negativas ao mesmo tempo.
let numero1 = -9;
let numero2 = -9;

// Use uma estrutura condicional (if-else) para verificar se ambas as variáveis são negativas.
if (numero1 < 0 && numero2 < 0) {
    console.log(true); // Se ambas as variáveis são negativas, imprime 'true'.
} else {
    console.log(false); // Caso contrário, imprime 'false'.
}
