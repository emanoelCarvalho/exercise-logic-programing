/*Identificar se um número é divisível por 3 e 2;*/

// Defina o número que você deseja verificar se é divisível por 3 e 2.
let numero = 12;

// Use uma estrutura condicional (if-else) para verificar a divisibilidade.
if (numero % 3 === 0 && numero % 2 === 0) {
    console.log('É divisível.'); // Se o número for divisível por 3 e 2, imprime 'É divisível'.
} else {
    console.log('Não é divisível.'); // Caso contrário, imprime 'Não é divisível'.
}
