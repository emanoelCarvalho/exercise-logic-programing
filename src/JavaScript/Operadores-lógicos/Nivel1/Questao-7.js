/*Identificar quando duas variáveis são positivas ao mesmo tempo;*/

// Defina as duas variáveis que você deseja verificar se são positivas ao mesmo tempo.
let numero1 = 8;
let numero2 = 8;

// Use uma estrutura condicional (if-else) para verificar se ambas as variáveis são positivas.
if (numero1 > 0 && numero2 > 0) {
    console.log(true); // Se ambas as variáveis são positivas, imprime 'true'.
} else {
    console.log(false); // Caso contrário, imprime 'false'.
}
