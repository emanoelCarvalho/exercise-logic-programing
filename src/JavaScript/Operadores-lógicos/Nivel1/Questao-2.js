/*Identificar se um número é divisível por 5 e 4;*/

// Defina o número que você deseja verificar se é divisível por 5 e 4.
let numero = 20;

// Use uma estrutura condicional (if-else) para verificar a divisibilidade.
if (numero % 5 === 0 && numero % 4 === 0) {
    console.log('É divisível.'); // Se o número for divisível por 5 e 4, imprime 'É divisível'.
} else {
    console.log('Não é divisível.'); // Caso contrário, imprime 'Não é divisível'.
}
