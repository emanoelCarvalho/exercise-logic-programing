/*Identificar se um número está entre 0 e 9 (intervalo inclusivo);*/

// Defina o número que você deseja verificar se está entre 0 e 9 (inclusive).
let numero = 3;

// Use uma estrutura condicional (if-else) para verificar se o número está no intervalo.
if (numero >= 0 && numero <= 9) {
    console.log(numero , ' Está entre 0 e 9'); // Se o número estiver no intervalo, imprime a mensagem apropriada.
} else {
    console.log(numero , ' Não está entre 0 e 9'); // Caso contrário, imprime a mensagem apropriada.
}
