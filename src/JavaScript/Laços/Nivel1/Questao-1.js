/*Imprimir os números negativos de 1 em 1, de -1 até -20;*/


// Inicialize o número com o valor inicial de -1.
let numero = -1;

// Use um loop do-while para imprimir números negativos de -1 até -20.
do {
    console.log(numero); // Imprime o valor atual de 'numero'.
    numero--; // Decrementa o valor de 'numero' em 1 a cada iteração.
} while (numero >= -20); // Continua o loop enquanto 'numero' for maior ou igual a -20.

