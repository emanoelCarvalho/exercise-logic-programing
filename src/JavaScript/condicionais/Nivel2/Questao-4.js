/*Dada a temperatura, classificá-la como fria (< 15), quente (> 30) e normal (caso contrário);*/

// Declarando uma variável 'temperatura' com o valor 30.
let temperatura = 30;

// Utilizando uma estrutura condicional para classificar a temperatura.
if (temperatura < 15) {
    console.log('Frio');
} else if (temperatura > 30) {
    console.log('Quente');
} else {
    console.log('Temperatura normal');
}
