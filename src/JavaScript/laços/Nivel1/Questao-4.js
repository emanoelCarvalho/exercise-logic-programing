// Use um loop for para imprimir os 10 primeiros números ímpares.
for (let i = 0; i <= 20; i++) {
    if (i % 2 != 0) { // Verifica se 'i' é ímpar (quando o resto da divisão por 2 não é zero).
        console.log(i); // Imprime 'i' se for ímpar.
    }
}
