/* Multiplicar dois números e verificar se o resultado é um número negativo; */

let numero1 = 3;
let numero2 = -3;

let multiplicar = numero1 * numero2;

/*Declarei, variáveis*/

if (multiplicar < 0) {
    console.log('É negativo');
} else {
    console.log('É positivo');
}
/*Fiz uma condicional, para testar se a variável, multiplicar é negativa, caso for, entrará no if*/